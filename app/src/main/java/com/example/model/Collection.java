package com.example.model;

public class Collection {

    private String cltImage;
    private String cltName;
    private String cltNumber;

    public Collection() {
        this.cltImage = cltImage;
        this.cltName = cltName;
        this.cltNumber = cltNumber;
    }

    public String getCltImage() {
        return cltImage;
    }

    public void setCltImage(String cltImage) {
        this.cltImage = cltImage;
    }

    public String getCltName() {
        return cltName;
    }

    public void setCltName(String cltName) {
        this.cltName = cltName;
    }

    public String getCltNumber() {
        return cltNumber;
    }

    public void setCltNumber(String cltNumber) {
        this.cltNumber = cltNumber;
    }

}
